    public void populateResidentsTable()
    {
     DefaultTableModel model = (DefaultTableModel)tblResidents.getModel();
        
        model.setRowCount(0);
        
        for (Employee employee : enterprise.getEmployeeDirectory().getEmployeeList()){
            if(employee instanceof Resident)
            {    
            Object[] row = new Object[3];
            row[0] =((Resident)employee).getName();
            row[1] = ((Resident)employee).getFloor();
            row[2]=(Resident)employee;
            model.addRow(row);
            }
        }   
    }
