    private void btnManageResidentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageResidentActionPerformed
        int selectedRow = tblResidents.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Select the row to manage profile");
            return;
        }

        Resident resident=(Resident)tblResidents.getValueAt(selectedRow,0);
        ViewResidentsProfileJPanel viewResidentsProfileJPanel=new ViewResidentsProfileJPanel(userProcessContainer,resident);
        userProcessContainer.add("viewResidentsProfileJPanel",viewResidentsProfileJPanel);
        CardLayout cardLayout=(CardLayout)userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);        
    }//GEN-LAST:event_btnManageResidentActionPerformed


