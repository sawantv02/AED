    public void sendsms(String number,String msg) throws Exception
    {
       
        String myPasscode = "vishakha";
        String myUsername = "sawantv02";    
        String toPhoneNumber =number;
        String myMessage = msg;
        String url="http://cloud.fowiz.com/api/message_http_api.php?username="+myUsername+"&phonenumber="+toPhoneNumber+"&message="+myMessage+"&passcode="+myPasscode;

        //myQuery = URLEncoder.encode(myQuery,"UTF-8");
            //String url = "http://www.myurl.com/?parameter="+myQuery;

            URL obj = new URL(url);
            HttpURLConnection con = (HttpURLConnection) obj.openConnection();

            // optional default is GET
            con.setRequestMethod("GET");
            con.setRequestProperty("Accept-Charset", "UTF-8");

            //add request header
            //private final String USER_AGENT = "Mozilla/5.0";
            //con.setRequestProperty("User-Agent", USER_AGENT);

            int responseCode = con.getResponseCode();
            System.out.println("\nSending 'GET' request to URL : " + url);
            System.out.println("Response Code : " + responseCode);

            BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream(),"UTF-8"));
            String inputLine;
            StringBuffer response = new StringBuffer();

            while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
            }
            in.close();

            //print result
            System.out.println(response.toString());
            con.disconnect();
            System.out.println(response.toString());
            
            if(responseCode==200)
              JOptionPane.showMessageDialog(null,"Message Sent!");
            else
                JOptionPane.showMessageDialog(null,"Message will reach shortly.");
    
        
    }

