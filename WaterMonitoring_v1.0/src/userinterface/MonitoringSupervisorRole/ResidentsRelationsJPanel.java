    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try{
        if(txtSubject.getText().trim().isEmpty() && txtMessage.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be Empty!");
            return;
        }
            
        else
        {   
        SendEmail se;
        if(txtTo.getText().equals("To All"))
        {    
            for(Employee employee:enterprise.getEmployeeDirectory().getEmployeeList())
            {
                if(employee instanceof Resident)
                {    
                se=new SendEmail(((Resident)employee).getEmail(),txtSubject.getText(),txtMessage.getText()); 
                try {
                    sendsms(((Resident)employee).getPhoneNumber(),txtMessage.getText());
                } catch (Exception ex) {
                    Logger.getLogger(ResidentsRelationsJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                }
            }
            JOptionPane.showMessageDialog(null,"Email sent successfully");
        }
        else 
        {
                se=new SendEmail(txtTo.getText(),txtSubject.getText(),txtMessage.getText());
            try {
                sendsms(txtContactNumber.getText(),txtMessage.getText());
            } catch (Exception ex) {
                Logger.getLogger(ResidentsRelationsJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        JOptionPane.showMessageDialog(null,"Email sent successfully");
        }
        }
        txtMessage.setText("");
        txtSubject.setText("");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Please try again after some time.Issue with the messaging server.");
        }
    }//GEN-LAST:event_btnSendActionPerformed

