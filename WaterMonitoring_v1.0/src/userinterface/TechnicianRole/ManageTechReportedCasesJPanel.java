    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        request.setAssignedTo(userAccount);
        request.setStatus("Assigned");
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Select the row to process.");
            return;
        }

        WaterPipeLeakWorkRequest request = (WaterPipeLeakWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        if(request.getStatus()==null|| request.getStatus()=="Completed")
        //request.setStatus("processing");
        JOptionPane.showMessageDialog(null,"Request cannot be processed.Its either not Assigned or Already Completed!");
        else
        {    
        ProcessTechWorkRequestJPanel processWorkRequestJPanel = new ProcessTechWorkRequestJPanel(userProcessContainer, request,userAccount);
        userProcessContainer.add("ProcessTechWorkRequestJPanel", processWorkRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_processJButtonActionPerformed

