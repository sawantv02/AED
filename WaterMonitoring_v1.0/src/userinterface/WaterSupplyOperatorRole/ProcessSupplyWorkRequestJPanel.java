    public void populateCaseCategory()
    {
        caseCategoryComboBox.removeAllItems();
        for(String caseCategory:request.getCaseCategory())
            caseCategoryComboBox.addItem(caseCategory);
        if(request.getCategory()!=null)
            caseCategoryComboBox.setSelectedItem(request.getCategory());
    
    }
    
    public void populateCaseStatus()
    {
        StatusComboBox.removeAllItems();
        for(String status:request.getStatusList())
            StatusComboBox.addItem(status);
        if(request.getStatus()!=null)
        {
            StatusComboBox.setSelectedItem(request.getStatus());
            txtDescription.setText(request.getDescription());
            txtComment.setText(request.getComment());
        }
    
    }
    public void populateData()
    {
        lblCaseID.setText(String.valueOf(request));
        lblAssignedTo.setText(request.getAssignedTo().getEmployee().getName());
        lblCaseLocation.setText(request.getLocation());
    }
