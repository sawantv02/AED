    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        if(!txtDescription.getText().trim().isEmpty() && !txtComment.getText().trim().isEmpty())
        {
        request.setStatus(StatusComboBox.getSelectedItem().toString());
        request.setDescription(txtDescription.getText());
        request.setCategory(caseCategoryComboBox.getSelectedItem().toString());
        request.setComment(txtComment.getText());  
        if(request.getStatus().equals("Completed"))
        request.setResolveDate(new Date());
        request.setMessage(txtMessage.getText());
        JOptionPane.showMessageDialog(null,"Request processed successfully");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be Empty!");
            txtDescription.setText("");
            txtComment.setText("");
            txtDescription.grabFocus();
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    public int[] checkLocation()
    {
      String location=request.getLocation();
      String building=request.getBuilding().getBuildingName();
      if(building.contains("1171"))
      {
          if(location.contains("Top"))
           x[0]=50;
          else
              x[0]=550;
          x[1]=250;
      }
            if(building.contains("1167"))
      {
           if(location.contains("Top"))
           x[0]=50;
          else
              x[0]=550;
          x[1]=620;
      }
       return x;     
    }
    
